interface Data {
  id: number;
  organization: string;
  username: string;
  email: string;
  phone_number: number;
  date_joined: string;
  status: string;
}

function createData(
  id: number,
  organization: string,
  username: string,
  email: string,
  phone_number: number,
  date_joined: string,
  status: string
): Data {
  return {
    id,
    organization,
    username,
    email,
    phone_number,
    date_joined,
    status,
  };
}

function getRandomInt(min: number, max: number): number {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function getRandomOrganization(): string {
  const organizations = ["Lendstart", "Lendsqr", "Ilorun"];
  return organizations[getRandomInt(0, organizations.length - 1)];
}

function getRandomStatus(): string {
  const statuses = ["inactive", "active", "pending", "blacklisted"];
  return statuses[getRandomInt(0, statuses.length - 1)];
}

